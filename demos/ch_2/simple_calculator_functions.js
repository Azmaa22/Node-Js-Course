function addTwoNumber(a, b) {
  return a + b;
}
function subTwoNumber(a, b) {
  return a - b;
}
function multiplyTwoNumber(a, b) {
  return a * b;
}
function dividedTwoNumber(a, b) {
  return a / b;
}

module.exports = {
  add: addTwoNumber,
  sub: subTwoNumber,
  multiplier: multiplyTwoNumber,
  divider: dividedTwoNumber,
};
